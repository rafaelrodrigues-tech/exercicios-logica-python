#Imprimir um calendario. ex 1. janeiro.

mes = int(input("Digite um número que represente um mês a seguir entre 1 e 12: \n"))

if mes == 1:
   print("Janeiro.")

elif mes == 2: 
   print("Fevereiro.")  

elif mes == 3: 
   print("Março.")

elif mes == 4: 
   print("Abril.") 

elif mes == 5: 
   print("Maio.") 

elif mes == 6: 
   print("Julho.")     

elif mes == 7: 
   print("Junho.") 

elif mes == 8: 
   print("Agosto.") 

elif mes == 9: 
   print("Setembro.") 

elif mes == 10: 
   print("Outubro.") 

elif mes == 11: 
   print("Novembro.") 

elif mes == 12: 
   print("Dezembro.")         

else: 
   print("Número Invalido, Digite um número entre 1 e 12.")             